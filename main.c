#define CHECK_SDL_CODE(Code)                                 \
    do {                                                     \
        int code = Code;                                     \
        if (code != 0) {                                     \
            fprintf(stderr, #Code ": %s\n", SDL_GetError()); \
            exit(1);                                         \
        }                                                    \
    } while (false)

static void* check_sdl_pointer(void* ptr, const char* call) {
    if (ptr == NULL) {
        fprintf(stderr, "%s: %s\n", call, SDL_GetError());
        exit(1);
    }
    return ptr;
}

#define CHECK_SDL_POINTER(Ptr) check_sdl_pointer(Ptr, #Ptr)

